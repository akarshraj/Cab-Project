    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int c_x = Integer.parseInt(p_c_x.getText());
        int c_y = Integer.parseInt(p_c_y.getText());
        int d_x = Integer.parseInt(p_d_x.getText());
        int d_y = Integer.parseInt(p_d_y.getText());
        pd.setCurrentLocation(c_x, c_y);
        pd.setDropLocation(d_x, d_y);
        /*
        Location loc = pd.getCurrentLocation();
        int x = loc.getX();
        int y = loc.getY();
        System.out.println(x+" "+y);
        */
        //find driver.
        DriverDetails dd;
        DriverList dl = new DriverList();
        dd = dl.findDriver(pid);
        if(dd == null)
            l2.setText("Sorry! No Driver Found");
        else
        {
            int did = dd.getD_id();
            l2.setText("Happy Journey");
            l3.setText(dd.getD_name());
            l4.setText(dd.getD_phone());
            dl.setPassengerForDriver(did, pid, c_x, c_y);
            dd.setCurrentLocation(c_x, c_y);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

